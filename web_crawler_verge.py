"""# Utils"""

def normalize_url(url: str) -> Optional[str]:
  try:
    parsed = urlparse(url.strip())
    if parsed.scheme not in ("http", "https"):
      return None
    if not parsed.netloc:
      return None

    scheme = parsed.scheme.lower()
    hostname = parsed.hostname.lower()
    path = parsed.path or "/"
    if path !="/" and path.endswith("/"):
      path = path[:-1]

    return urlunparse((scheme, hostname, path, "", parsed.query, ""))
  except Exception:
    return None


def is_allowed_domain(url:str, allowed_domains: Set[str]) -> bool:
  parsed = urlparse(url)
  if parsed.hostname is None:
    return False
  hostname = parsed.hostname.lower()
  for allowed_domain in allowed_domains:
    if hostname == allowed_domain or hostname.endswith("." + allowed_domain):
      return True
  return False


def extract_links(base_url: str, html: str) -> list[str]:
  soup = BeautifulSoup(html, "html.parser")
  links = []

  for tag in soup.find_all("a", href = True):
    href = tag["href"]
    normalized_url = normalize_url(urljoin(base_url, href))
    if normalized_url:
      links.append(normalized_url)

  return links

